//Q10. Swap the value...
#include<stdio.h>
int main()
{
    int input_1;
    int input_2;

    printf("Enter values to swap\n");
    printf("First value:");
    scanf("%d", &input_1);
    printf("Second value:");
    scanf("%d", &input_2);

    printf("\t\t   Swapped values\n");
    printf("\t\tFirst value is %d\n", input_2);
    printf("\t\tSecond value is %d", input_1);

    return 0;
}
